# Завдання 6

clients = {
    "ID": [1, 2, 3, 4, 5],
    "Ім'я": ["Іван", "Марія", "Петро", "Олена", "Андрій"],
    "Місто": ["Київ", "Львів", "Одеса", "Харків", "Дніпро"]
}

df_clients = pd.DataFrame(clients)

orders = {
    "ID клієнта": [1, 2, 3, 1, 5],
    "Замовлення": [
        "Ноутбук",
        "Миша",
        "Монітор",
        "Клавіатура",
        "Навушники"
    ],
    "Вартість": [25000, 500, 8000, 1200, 3000]
}

df_orders = pd.DataFrame(orders)

print("Клієнти:")
print(df_clients)

print("\nЗамовлення:")
print(df_orders)

df = pd.merge(
    df_clients,
    df_orders,
    left_on="ID",
    right_on="ID клієнта"
)

print("\nОб'єднаний DataFrame:")
print(df)

result = (
    df.groupby("Ім'я")["Вартість"]
    .sum()
    .reset_index()
)

print("\nЗагальна вартість замовлень:")
print(result)