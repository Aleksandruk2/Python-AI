import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.metrics import accuracy_score, classification_report
from sklearn.model_selection import train_test_split, cross_val_score
from sklearn.preprocessing import LabelEncoder
from sklearn.tree import DecisionTreeClassifier, plot_tree

print("----Привіт! Козаки і козачки. Дерева це круто---")

RANDOM_STATE = 42

np.random.seed(RANDOM_STATE)

# Генерація даних
N = 600

genders = np.random.choice(['Чоловіча', 'Жіноча'], N)
# print("Стать", len(genders))
birth_months = np.random.randint(1, 13, N)
# print("Місяці", len(birth_months))
# Час на дорогу на навчання
commute_time = np.random.randint(5, 121, N)

#Список предметів і оцінки по предметах
subjects = [
    "Математика",
    "Фізика",
    "Історія",
    "Мова",
    "Інформатика"
]

grades = {
    subj: np.random.randint(60, 101, size=N)
    for subj in subjects
}

# print("grades", grades)

#
df = pd.DataFrame({
    "Стать": genders,
    "Місяць_народження": birth_months,
    "Час_на_дорогу_хв": commute_time,
    **grades  # Це як деструктуризація у JS ...
})
# print("My df", df)

# subjects - беруться оцінки
# axis - середнє значення по рядку
df["Середній_бал"] = df[subjects].mean(axis=1).round(1)
# print("My df", df)

# Чим більше середнє значення, тим сильніше відрізняються оцінки студентів
grade_std = df[subjects].std(axis=1)

score = (

    # Якщо середній бал більший за 80,
    # то score збільшується.
    (df["Середній_бал"] - 80) * 0.6

    # Якщо студент витрачає багато часу на дорогу,
    # score трохи зменшується.
    - (df["Час_на_дорогу_хв"] - 60) * 0.05

    # Якщо оцінки дуже нестабільні (великий розкид),
    # score також зменшується.
    - grade_std * 0.3

    # Додаємо випадковий шум із нормального розподілу.
    # Це робить дані більш реалістичними,
    # оскільки успішність залежить не лише від відомих факторів.
    + np.random.normal(0, 6, size=N)
)

def to_class(s):

    # Якщо score достатньо високий,
    # прогнозуємо покращення успішності.
    if s > 4:
        return "Покращиться"

    # Якщо score дуже низький,
    # прогнозуємо погіршення успішності.
    elif s < -4:
        return "Погіршиться"

    # В усіх інших випадках
    # прогнозуємо стабільну успішність.
    else:
        return "Стабільно"

df["Успішність_наступного_семестру"] = (
    score.apply(to_class)
    if hasattr(score, "apply")
    else score.apply(to_class)
)

# print(df.head())

print("Розподіл класів цільової змінної")

print(df["Успішність_наступного_семестру"].value_counts(), '\n')

# Підготовка даних для моделі
# для кодування статті - 1 чоловіча, 0 жіноча
le_gender = LabelEncoder()

df["Стать_код"] = le_gender.fit_transform(df["Стать"])

le_target = LabelEncoder()

y = le_target.fit_transform(df["Успішність_наступного_семестру"])

feature_cols = [
    'Стать_код',
    'Місяць_народження',
    'Час_на_дорогу_хв',
    'Середній_бал'
] + subjects

X = df[feature_cols]

X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.2,
    random_state=RANDOM_STATE,
    # Зберігає однакову позицію класів у навчальній та тествій вибірці
    stratify=y,
)

# Створюємо словник моделей,
# які будемо навчати та порівнювати.
models = {

    # Перша модель — дерево рішень.
    "Дерево рішень": DecisionTreeClassifier(

        # Максимальна глибина дерева.
        # Чим більша глибина, тим складніша модель.
        max_depth=3,

        # Фіксоване зерно генератора випадкових чисел.
        random_state=RANDOM_STATE
    ),

    # Друга модель — Random Forest.
    # Це ансамбль багатьох дерев рішень.
    "Random Forest (ансамбль беггінгу)": RandomForestClassifier(

        # Кількість дерев у лісі.
        # Кожне дерево навчається незалежно.
        n_estimators=200,

        # Максимальна глибина кожного дерева.
        max_depth=6,

        # Фіксоване випадкове зерно.
        random_state=RANDOM_STATE
    ),

    # Третя модель — Gradient Boosting.
    # Це ансамбль, де кожне нове дерево
    # виправляє помилки попередніх.
    "Gradient Boosting (ансамбль бустингу)": GradientBoostingClassifier(

        # Кількість дерев.
        n_estimators=200,

        # Максимальна глибина кожного дерева.
        max_depth=3,

        # Швидкість навчання моделі.
        # Менше значення = повільніше,
        # але часто точніше навчання.
        learning_rate=0.1,

        # Фіксоване зерно випадкових чисел.
        random_state=RANDOM_STATE
    ),
}

# Створюємо порожній словник,
# у якому будемо зберігати точність моделей.
results = {}

# Перебираємо всі моделі зі словника.
# name — назва моделі.
# model — сама модель.
for name, model in models.items():

    # Навчаємо модель на навчальній вибірці.
    model.fit(X_train, y_train)

    # Отримуємо прогноз
    # для тестової вибірки.
    pred = model.predict(X_test)

    # Обчислюємо точність моделі.
    acc = accuracy_score(y_test, pred)

    # Виконуємо 5-кратну крос-валідацію.
    # Дані діляться на 5 частин,
    # модель навчається і перевіряється 5 разів,
    # після чого обчислюється середня точність.
    cv_scores = cross_val_score(
        model,
        X,
        y,
        cv=5
    )

    # Зберігаємо точність моделі
    # у словнику результатів.
    results[name] = acc

    # Виводимо назву моделі.
    print(f"=== {name} ===")

    # Виводимо точність
    # на тестовій вибірці.
    print(f"Точність на тесті: {acc:.3f}")

    # Виводимо середню точність
    # крос-валідації та стандартне відхилення.
    print(
        f"Крос-валідація (5-fold): "
        f"{cv_scores.mean():.3f} ± {cv_scores.std():.3f}"
    )

    # Виводимо детальний звіт
    # про якість класифікації.
    print(
        classification_report(
            y_test,
            pred,
            target_names=le_target.classes_
        )
    )

    # Виводимо роздільну лінію.
    print("-" * 60)

# Отримуємо модель Random Forest,
# яку вже було навчено.
rf = models["Random Forest (ансамбль беггінгу)"]

# Створюємо об'єкт Series,
# де індексами є назви ознак,
# а значеннями — їх важливість.
# feature_importances_ обчислюється автоматично
# після навчання Random Forest.
importances = pd.Series(

    # Значення важливості кожної ознаки.
    rf.feature_importances_,

    # Назви ознак.
    index=feature_cols

# Сортуємо ознаки
# від найважливішої до найменш важливої.
).sort_values(ascending=False)

# Виводимо заголовок.
print("Важливість ознак (Random Forest):")

# Виводимо відсортований список
# важливості ознак.
print(importances, "\n")

# Створюємо рисунок із двома областями (підграфіками),
# розташованими в один ряд і два стовпці.
# figsize=(18, 7) задає розмір рисунка у дюймах.
fig, axes = plt.subplots(1, 2, figsize=(18, 7))

# --------------------------------------------------------------------
# Побудова дерева рішень
# --------------------------------------------------------------------

# Відображаємо навчане дерево рішень.
plot_tree(

    # Модель дерева рішень,
    # яку було навчено раніше.
    models["Дерево рішень"],

    # Назви ознак,
    # що використовувалися під час навчання.
    feature_names=feature_cols,

    # Назви класів цільової змінної.
    class_names=le_target.classes_,

    # Зафарбовуємо вузли дерева
    # відповідно до прогнозованого класу.
    filled=True,

    # Робимо прямокутники
    # із заокругленими кутами.
    rounded=True,

    # Розмір шрифту
    # усередині дерева.
    fontsize=7,

    # Малюємо дерево
    # у першій області рисунка.
    ax=axes[0],
)

# Встановлюємо заголовок
# першого графіка.
axes[0].set_title("Дерево рішень (max_depth=4)")

# --------------------------------------------------------------------
# Побудова графіка важливості ознак
# --------------------------------------------------------------------

# Будуємо горизонтальну стовпчикову діаграму.
importances.plot(

    # barh = горизонтальні стовпчики.
    kind="barh",

    # Малюємо графік
    # у другій області рисунка.
    ax=axes[1],

    # Колір стовпчиків.
    color="#4C72B0"
)

# Додаємо назву графіка.
axes[1].set_title("Важливість ознак (Random Forest)")

# Перевертаємо вісь Y,
# щоб найважливіша ознака
# була зверху.
axes[1].invert_yaxis()

# Підписуємо вісь X.
axes[1].set_xlabel("Важливість")

# Автоматично підбираємо відступи
# між графіками.
plt.tight_layout()

# Зберігаємо рисунок у файл PNG.
# dpi=150 визначає якість зображення.
plt.savefig("tree_and_importance.png", dpi=150)

# Повідомляємо користувача,
# що файл успішно збережений.
print("Графік збережено: tree_and_importance.png")


# --------------------------------------------------------------------
# Порівняння точності моделей
# --------------------------------------------------------------------

# Створюємо новий рисунок.
plt.figure(figsize=(7, 5))

# Будуємо стовпчикову діаграму.
plt.bar(

    # Назви моделей.
    results.keys(),

    # Їхня точність.
    results.values(),

    # Колір кожного стовпчика.
    color=["#55A868", "#4C72B0", "#C44E52"]
)

# Підписуємо вісь Y.
plt.ylabel("Точність (accuracy)")

# Встановлюємо заголовок графіка.
plt.title("Порівняння моделей на тестовій вибірці")

# Повертаємо підписи по осі X,
# щоб вони не накладалися.
plt.xticks(rotation=15, ha="right")

# Межі осі Y:
# від 0 до 1,
# оскільки accuracy вимірюється
# у цьому діапазоні.
plt.ylim(0, 1)

# Перебираємо всі значення точності.
for i, v in enumerate(results.values()):

    # Виводимо числове значення
    # над кожним стовпчиком.
    plt.text(

        # Позиція по X.
        i,

        # Позиція трохи вище стовпчика.
        v + 0.02,

        # Форматування числа
        # до двох знаків після коми.
        f"{v:.2f}",

        # Вирівнювання тексту.
        ha="center"
    )

# Автоматично вирівнюємо елементи графіка.
plt.tight_layout()

# Зберігаємо графік у файл.
plt.savefig("model_comparison.png", dpi=150)

# Повідомляємо про успішне збереження.
print("Графік збережено: model_comparison.png")

# ---------------------------------------------------------------------------
# 7. ПРИКЛАД ПРОГНОЗУ ДЛЯ НОВОГО СТУДЕНТА
# ---------------------------------------------------------------------------
# Створюємо новий DataFrame,
# який містить інформацію лише про одного студента.
new_student = pd.DataFrame([{

    # Перетворюємо текстове значення статі
    # у числовий код за допомогою LabelEncoder.
    # transform() повертає масив,
    # тому беремо перший елемент [0].
    "Стать_код": le_gender.transform(["Жіноча"])[0],

    # Місяць народження студента.
    "Місяць_народження": 5,

    # Час, який студент витрачає
    # на дорогу до навчального закладу.
    "Час_на_дорогу_хв": 25,

    # Середній бал студента.
    "Середній_бал": 88.4,

    # Оцінка з математики.
    "Математика": 90,

    # Оцінка з фізики.
    "Фізика": 85,

    # Оцінка з історії.
    "Історія": 88,

    # Оцінка з української мови.
    "Мова": 91,

    # Оцінка з інформатики.
    "Інформатика": 88,
}])

# Отримуємо прогноз моделі Random Forest.
# predict() повертає числовий код класу.
# inverse_transform() перетворює цей код
# назад у текстове значення.
# [0] бере перший (і єдиний) результат прогнозу.
pred_class = le_target.inverse_transform(
    rf.predict(new_student)
)[0]

# Отримуємо ймовірності
# належності студента до кожного класу.
# predict_proba() повертає масив,
# де кожен елемент — це ймовірність відповідного класу.
# [0] вибирає результат для першого студента.
pred_proba = rf.predict_proba(new_student)[0]

# Виводимо заголовок.
print("\nПрогноз для нового студента (Random Forest):")

# Виводимо прогнозований клас.
print(f"Прогнозований клас: {pred_class}")

# Перебираємо всі класи та їхні ймовірності.
for cls, p in zip(le_target.classes_, pred_proba):

    # Виводимо назву класу
    # та відповідну ймовірність.
    # :.2% означає форматування
    # у відсотках із двома знаками після коми.
    print(f"  {cls}: {p:.2%}")

# Зберігаємо весь створений датасет
# у CSV-файл.
df.to_csv(

    # Назва файлу.
    "students_dataset.csv",

    # Не записувати індекси рядків.
    index=False,

    # Кодування UTF-8 із BOM,
    # щоб українські символи
    # коректно відкривалися в Microsoft Excel.
    encoding="utf-8-sig"
)

# Повідомляємо користувача,
# що файл успішно збережено.
print("\nДатасет збережено: students_dataset.csv")